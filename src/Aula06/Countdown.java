package Aula06;


public class Countdown {

	public static void main(String[] args) {
		for (int i = 0; i < 6; i++) {
			System.out.println(i);
		}
		System.out.println("Fim da contagem");
		for (int i = 0; i < 21; i++) {
			if (i % 2 == 0) {
				System.out.println(i);
			}
			
		}
		System.out.println("Fim da contagem");

	}

}
